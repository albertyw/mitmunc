<br />
Accommodations:<br />
<p>
MITMUNC has again negotiated with the Cambridge Marriott hotel for lowered rates for conference attendees.  
The rates this year are $120 per room per night for up to quadruple occupancy.  Due to changes in room booking, 
delegations should email or call our contact at the Marriott, Nancy Lee, at nancy.m.lee@marriott.com or by phone at 617-494-6600, ext. 6746.  
Please note that reservations at the Marriott must be made on or before January 13, 2011.  More information is available online at <a href="http://www.mitmunc.org/accommodations">http://www.mitmunc.org/accommodations.php</a>.
</p>
<br />
Committees:<br />
<p>
We will have seven committees this year: The Advisory Panel on the Question of Darfur, the Commission on Science and Technology for Development, 
the UNDP, UNEP, UNSC, WHO, and WTO.  Of these, the UNSC will be a crisis committee.  <i>(Some text about crises have been redacted)</i>  
Additionally, we will have an advisory panel as a committee.  Unlike other committees, delegates on the advisory panel will be playing
the roles of certain people, not just representing countries.  I encourage your best delegates to apply for UNSC and advisory panel committees.  
Information about committees will be posted shortly at <a href="http://www.mitmunc.org/committee">http://www.mitmunc.org/committee.php</a>.
</p>
<br />
Finances:<br />
<p>
Like last year, your delegation's calculated fees are posted with an invoice on the control panel after you log in.  
Financial aid is also available for schools that apply.  If you would like a receipt for your payment, please contact us. 
</p>
<br />
Schedule:<br />
<p>
The schedule for MITMUNC will be similar to last year's.  We planning on having some workshops run by the MITMUNC 
staff including an MIT information session and a campus tour.  These workshops will probably be held at the same time 
as the Friday or Saturday night events and students will have the option of attending workshops or going to the events.  If you are 
interested in us providing other workshops, please tell us.  More information about the schedule can be found at <a href="http://www.mitmunc.org/schedule">http://www.mitmunc.org/schedule.php</a>. 
</p>
