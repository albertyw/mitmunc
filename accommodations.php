<h2>Parking/Transportation</h2>
<p>We recommend using the Boston T subway to get to the conference.  There is a T stop (Kendall/MIT) very close to 
the conference at the entrance of the Marriott hotel.  The T's route map can be found on their 
<a href="http://www.mbta.com/schedules_and_maps/subway/">website</a>.  Additionally, if you are flying into Logan International Airport, 
it is quite easy to take the Silver Line Bus to South Station, then transfer to the Red Line subway towards Alewife Station to get to Kendall/MIT.  A taxi ride between 
MIT and Logan Airport will cost between $20 and $30 dollars.</p>  
<p>For drivers, the Marriott has a paid parking garage near their hotel at $23/night.  
However, after 5 PM on Friday and on weekends, there is free parking in the 
<a href="http://whereis.mit.edu/?selection=P4&zoom=18" target="_blank">
parking lot outside of E51</a>, the conference's main building.  
Before 5 PM Friday, we would recommend curbside parking near E51 or parking at the Marriott.</p>

<h2>Internet Access</h2>
<p>Free Guest Wireless Internet access is available on campus for those who bring Wi-Fi enabled electronics.  
Paid Wireless Internet access is available in the Marriott hotel too.  Please note however that delegates are not allowed to use electronics in committee session.  </p>
