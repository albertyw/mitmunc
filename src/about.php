<p>
<b>MITMUNC</b> believes in public debate and its potential to empower the future 
leaders of the world. Every year, in the month of February, we give
these young leaders of tomorrow an opportunity to engage in public debate,
through our innovative Model United Nations conference. Many of today's issues
- international security, global health, alternative energy sources - are all
based on scientific principles and are rapidly changing as our technology
develops further every day. So, we feature students from our institute as the
staff for the conference, using their expertise to make the scientific issues
accessible to the students during debate. As a result, our conference has a
unique scientific touch. 
</p>
<p>
The MITMUNC logo is the MIT seal encased by the United Nations olive branches,
representing the union of our institute with international goals for peace and
collaboration. The MIT seal pictures a scholar and a worker, and contains the
phrase "Mens et Manus", or mind and hand. MIT has long championed the
intersection of academic knowledge and practical skills, and we will bring that
same spirit to our conference.
</p>
<p>
MITMUNC began in Spring 2009. Until 2008, we had co-hosted <a href = "
http://www.bosmun.org/">Boston Invitational Model United Nations Conference</a>,
but now, with our conference, we can't wait for you to see what we have in
store.
</p>
<p>
