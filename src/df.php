<p>Delegations with fewer or equal to five delegates and one adviser are exempt 
from school fees only.</p>

<p>The delegate fees are refundable if we receive a request for refund before January 15, 2016.  The school fee is not refundable, unless you paid in attempts to move off the waitlist but were not given a spot in our conference. After January 15, 2016, if circumstances beyond your control result in your delegation bringing fewer students to the conference than the number for which you paid, your school is eligible for a 50% refund on those students' delegate fees.</p>

<p>
In order for MITMUNC to be an amazing and diverse conference with ample opportunity 
for participation for all delegates, 
we have a policy that every delegation can bring at maximum 30 delegates to the conference.  We will not make any exceptions to this rule.
</p>

<p>
Financial assistance is available.  The deadline for financial aid applications is December 1, 2015.  
The financial aid application consists of five short questions that allow us to gauge 
how much financial aid will be given.  The application will be available to fill out in 
the control panel after you have completed registration.  
Please note that financial aid can only cover school and delegate fees.  If travel, hotel, food, or other costs are proving to be burdensome to your school, please feel free to contact us for additional assistance and guidance. 
</p>

