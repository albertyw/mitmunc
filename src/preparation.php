<h1>Preparing for MITMUNC</h1>

<h2>Position Papers</h2>
<p>Position papers are due by email to delegates' respective chairs (see the committee pages for email addresses) by Saturday, January 31, at noon.  They are required for every committee and 
are a prerequisite for consideration for delegate awards.</p>

<p>The position paper is a pre-conference document that should be a statement of each delegates' position in the conference in an organized and logical format.  
Not only will position papers allow delegates to better understand the topic being discussed and their individual countries' stance on the committee topics, 
but it also forms as an effective base to begin speeches and discussions during the actual conference.
Position papers should describe their countries' positions, both public and private, in terms of the committee, to their chairs in succinct paragraphs.</p>

<p><a href="/files/positionPaperTemplate.doc">Position Paper Template</a></p>

<h3>Layout</h3>
<p>The position paper should begin with an introductory statement about the country's background in relation to the delegates' committee.  
The country background statement can include items about the country's past relation with the committee, major decisions by the committee that have affected the country, etc.  
General statements about the country's history are not needed; given the length of the position paper, the background statement should be focused on the country's relation to the field of expertise of the committee.</p>

<p>The majority of the position paper should be at least two paragraphs that separately describe the delegates' position on each of the committee topics.  
Delegates should first read their committees' background guides, posted on their committee pages, as they clarify and provide an introduction to the topics and may include pointers for what to write in the position paper.  
The position paper should explain the impact of the topic on the country, the country's stance on the topic, and the past actions relating to the topic that the country has undertaken.  </p>

<h3>Length</h3>
<p>The whole position paper (both topics) should be a maximum of one page of normal-sized single spaced text or the equivalent.  The short length requirement for our position papers is to allow delegates to practice 
condensing their thoughts into succinct papers and to allow chairs to be able to read every position paper thoroughly.  Bibliography and/or Works Cited are not required but may be included on a separate page.  
The header of each paper should list the committee, country, school, and delegate name.  </p>

<h3>Style</h3>
<p>The position paper should be written in the third person; refer to what your country and/or sovereign wishes, not to yourself.  For example: "The People's Republic of China wishes," "His Excellency demands," or 
"The people of Guatemala believe."  "We" is also acceptable.  However, as delegates and not heads of state, it would be improper to state "I want" or "I  believe" since you are not in the position to dictate personal beliefs 
on behalf of your country, whether or not they align.  This usage of third-person carries over to debate in committees and in resolutions and working papers.</p>

