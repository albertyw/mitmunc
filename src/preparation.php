conference materials they wish to bring to the conference.  </li>
<li>Delegates and advisers should bring a cell phone if possible.  The main 
MITMUNC phone number during the conference is <a href="/contact">601-564-8686</a>.</li>
<li>Delegates and advisers should bring money to buy food during the conference.  
We will provide refreshments during the Friday and Saturday night events,
