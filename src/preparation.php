<h2>Dress Code</h2>
<p>MITMUNC requires all delegates to be in formal western business attire 
during all committee sessions, opening, and closing ceremonies.  Western business attire
 for MITMUNC will be defined as a suit, tie, and a jacket (optional) or blouse, with 
 skirt or trousers.  We will not allow jeans or sneakers in committee.  Non-formal attire
  may be worn outside of committee sessions 
during the social events.  </p>

<h2>Parliamentary Procedure</h2>
<p>MITMUNC's parliamentary procedure is similar to HMUNC's.  We do not run our 
committees following THIMUN rules of procedure.  However, MITMUNC rules of 
procedure have been modified somewhat in order for committees to run more 
smoothly.  Our largest change is that working papers are not used in committee; 
writing working papers then resolutions tends to absorb too much committee time 
which would be better spent debating.  We also disallow pre-written resolutions 
in whole or in part because they supplant good debate.  
</p>
<p>MITMUNC will run a parliamentary procedure training workshop before Opening Ceremonies.  
(<a href="/schedule">See Conference Schedule</a>)
We recommend that new delegates attend to learn the rules of procedure before 
the actual committee sessions.  Experienced delegates may also want to 
attend to learn rules specific to MITMUNC.</p>

