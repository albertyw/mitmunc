<p>
Cambridge and Boston offer a variety of hotels at which to stay to experience all Boston has to offer. To learn more about the nearest hotel to the conference site, including the discounted rooms we have to offer you, please visit our <a href="http://mitmunc.mit.edu/accommodations">Accommodations page</a>.
<p>
If you would like to take MIT Admission's official tour, their website is at 
<a href="http://www.mitadmissions.org">mitadmissions.org</a>.  MIT Admissions runs 
tours every weekday at 10 AM and 3 PM.  
</p>
Additionally, here are our staff's recommendations of some of their favorite things to do in Boston:<br />
<ul>
<li><a href="http://web.mit.edu/museum/">MIT Museum</a></li>
<li><a href="http://www.thefreedomtrail.org/">The Freedom Trail</a> (Includes such famous sites as the USS Constitution, the Bunker Hill Monument, Paul Revere's House, the Old North Church, and the site of the Boston Massacre)</li>
<li><a href="http://www.faneuilhallmarketplace.com/">Fanneuil Hall</a></li>
<li><a href="http://www.thefreedomtrail.org/visitor/boston-common.html">Boston Commons</a></li>
<li><a href="http://www.mfa.org/">Museum of Fine Arts</a></li>
<li><a href="http://www.mos.org/">Museum of Science</a></li>
<li><a href="http://www.neaq.org/">New England Aquarium</a></li>
<li><a href="http://www.prudentialcenter.com/">Boylston Street and the Prudential Center</a></li>
<li><a href="http://www.prudentialcenter.com/dine/dine_detail.php?id=61">Top of the Hub Restaurant</a>
on the top floor of the Prudential Center</li>
</ul>
<p>
While journeying around the city, we highly recommend the use of Boston's Subway System, 
locally known as the "T".  
Maps, schedules, and fares can be found at the <a href="http://www.mbta.com/">MBTA website</a>.
</p>
