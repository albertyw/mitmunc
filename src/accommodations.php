The MIT Model United Nations Conference Secretariat and staff have been hard at work since the early fall planning and coordinating every element of the conference to ensure our delegates have an enriching and successful experience.
<p>
As to logistics, we are pleased to announce that MITMUNC 2016 will be held at the Boston Sheraton Hotel. After analyzing a variety of factors including transportation, safety, delegate experience, and weather provisions, the Secretariat made this decision. The Boston Sheraton Hotel provides a great environment for the conference and centralizes the whole MITMUNC experience. This year, we are also planning engaging crises events in committees, so having delegates ready to respond to a call of action at certain times may be crucial.
<p>

MITMUNC has coordinated special rooming rates with the Boston Sheraton Hotel. As such, advisers and their students are strongly advised to book their stay at the Boston Sheraton as soon as possible. 
<p>
    Booking can be done online at <a href="https://www.starwoodmeeting.com/Book/MITMUN16" target="_blank">this website</a> or by emailing 0430-reservations@starwoodhotels.com
    Alternatively, booking can be performed by phone at 617-236-6066. You may identify with group name "MIT Model UN” or group code “MA30AA”.
    We recommend having students in quads or triples. Advisers may book single or double occupancy rooms. The very latest a reservation can be finalized is by January 12th, 2016.
<p>
To recapitulate, MITMUNC 2016 will be held at the Boston Sheraton Hotel throughout the dates of Friday, February 5th through Sunday, February 7th, 2016. Country assignments will be released within two weeks. A more in-depth schedule detailing the conference events and committees will be sent later on.

